#include<iostream>
#include<string>

using namespace std;

class Person
{
private:
    string name;
    string dob;

public:
    Person(string name = "empty", string dob = "xx-xx-xx")
    {
        this->name = name;
        this->dob = dob;
    }

    ~Person()
    {

    }

    string toString() const
    {
        return "Name: " + this->name + " Date of birth: " + this->dob;
    }
};

int main()
{
    const int cap = 10;
    Person persons[cap];

    persons[0] = Person("BOB", "90-05-12");
    persons[1] = Person("JOHN", "90-05-12");
    persons[2] = Person("MAC", "90-05-12");

    for (size_t i = 0; i < cap; i++)
    {
        cout << persons[i].toString() << endl;
    }

    system("PAUSE");

    return 0;
}
