#include<iostream>
#include<vector>

using namespace std;

void func(vector<int> &a) //Passed by Reference
{
    a.push_back(5);
    a.push_back(6);
    a.push_back(7);
    a.push_back(8);
}

int main()
{
    vector<int> myVec;

    myVec.push_back(1);
    myVec.push_back(2);
    myVec.push_back(3);
    myVec.push_back(4);

    func(myVec);

    for (size_t i = 0; i < myVec.size(); i++)
    {
        cout << i << ": " << myVec[i] << endl;
    }

    system("PAUSE");

    return 0;
}
