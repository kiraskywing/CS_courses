#include<iostream>
#include<string>
#include<vector>

using namespace std;

class Person
{
private:
    //Member variables
    string name;
    string dateOfBirth;
    int age;

public:
    //Constructor
    Person() //Default constructor
    {
        this->name = "NONE";
        this->dateOfBirth = "xx-xx-xx";
        this->age = 0;
    }

    Person(string name, string dateOfBirth, int age) //Optional Constructor
    {
        this->name = name;
        this->dateOfBirth = dateOfBirth;
        this->age = age;

        cout << "PERSON CREATED!" << endl;
    }

    //Destructor
    ~Person()
    {
        cout << "DESTRUCTOR CALLED FOR " << this->name << endl;
    }

    //Member functions
    void printOut()
    {
        cout << "Name: " << this->name << " DOB: " << this->dateOfBirth << " Age: " << this->age << endl;
    }

protected:

};

int main()
{
    Person p1("BOB", "90-12-02", 20);
    Person p2("JOHN", "90-12-15", 230);

    string str();
    vector<int> intVec;
    char *c = nullptr;
    int i = 0;
    double d = 0.0;
    float f = 0.0f;
    bool b = false;

    p1.printOut();
    p2.printOut();

    return 0;
}
